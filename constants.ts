// Expanded to 8 cards to support up to 255 surnames
export const TOTAL_CARDS = 8;

// Function to generate the list of surnames for a specific card (bit position)
// Now accepts the source list (Common or Rare)
export const getSurnamesForCard = (cardIndex: number, surnameList: string[]): string[] => {
  const mask = 1 << cardIndex;
  const cardSurnames: string[] = [];

  for (let i = 1; i < surnameList.length; i++) {
    if ((i & mask) !== 0) {
      // Use the surname if available
      const name = surnameList[i];
      if (name) {
        cardSurnames.push(name);
      }
    }
  }
  return cardSurnames;
};